double calculate_addition(double arg1, double arg2)
{
	double result=arg1+arg2;
	return result;
}

double calculate_subtraction(double arg1, double arg2)
{
	double result=arg1-arg2;
	return result;
}

double calculate_multiplication(double arg1, double arg2)
{
	double result=arg1*arg2;
	return result;
}

double calculate_division(double arg1, double arg2)
{
	double result=arg1/arg2;
	return result;
}